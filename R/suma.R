#' Suma dos numeros
#'
#' La funcion `suma()` toma 2 numeros positivos y los suma.
#'
#' Estos son detalles extras de la funcion.
#'
#' @param x un vector numerico
#' @param y un vector numerico
#'
#' @return
#' Un vector numerico con la suma de `x` e `y`.
#'
#' @examples
#' suma(2, 2)
#' suma(1, 10)
#'
#' @export
suma <- function(x, y) {
  x + y
}
